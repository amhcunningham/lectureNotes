//Assignment Operator
let x = 20;

//addition assignment operator
x += 1;
console.log(x)
//take x and add the value of 1

//subtraction assignment operation
x -= 1;
console.log(x)
//take x and subtract the value of 1

//multiplication
x *= 2
console.log(x)

//division
x /= 2;
console.log(x)

//remainder (modulus)
x %= 2
console.log(x)

//modulus operator
console.log(5%3) // 5/3 equals 1 2/3 -> 2
console.log(25%7) // 25/7 equals 3 4/7 -> 4

//exponential assignment operator
x **= 2;
console.log(x)

let y = 7;
y **= 3;
console.log(y)